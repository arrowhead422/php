<article><p>hA preposition describes a relationship between other words in a sentence. In itself, a word like "in" or "after" is rather meaningless and hard to define in mere words. For instance, when you do try to define a preposition like "in" or "between" or "on," you invariably use your hands to show how something is situated in relationship to something else. Prepositions are nearly always combined with other words in structures called prepositional phrases. Prepositional phrases can be made up of a million different words, but they tend to be built the same: a preposition followed by a determiner and an adjective or two, followed by a pronoun or noun (called the object of the preposition). This whole phrase, in turn, takes on a modifying role, acting as an adjective or an adverb, locating something in time and space, modifying a noun, or telling when or where or under what conditions something happened.</p></article>


